<template>
  <div>
    <h3>1.基础用法</h3>
    <p>评分被分为三个等级，可以利用颜色对分数及情感倾向进行分级（默认情况下不区分颜色）。三个等级所对应的颜色用过colors属性设置，而它们对应的两个阈值则通过 low-threshold 和 high-threshold
      设定。</p>
    <div class="block">
      <span class="demonstration">默认不区分颜色</span>
      <el-rate v-model="value1"></el-rate>
    </div>
    <div class="block">
      <span class="demonstration">区分颜色</span>
      <el-rate
        v-model="value2"
        :colors="['#99A9BF', '#F7BA2A', '#FF9900']">
      </el-rate>
    </div>
    <h3>2.辅助文字</h3>
    <p>用辅助文字直接地表达对应分数</p>
    <p>为组件设置 show-text 属性会在右侧显示辅助文字。通过设置 texts 可以为每一个分值指定对应的辅助文字。texts 为一个数组，长度应等于最大值 max。</p>
    <el-rate
      v-model="value3"
      show-text>
    </el-rate>
    <h3>3.其它 icon</h3>
    <p>当有多层评价时，可以用不同类型的 icon 区分评分层级</p>
    <p>设置icon-classes属性可以自定义对应 3 个不同分段的图标。本例还使用void-icon-class指定了未选中时的图标类名。</p>
    <el-rate
      v-model="value4"
      :icon-classes="['el-icon-delete', 'el-icon-delete', 'el-icon-delete']"
      void-icon-class="el-icon-delete"
      :colors="['#99A9BF', '#F7BA2A', '#FF9900']">
    </el-rate>
    <h3>4.只读</h3>
    <p>只读的评分用来展示分数，允许出现半星</p>
    为组件设置 disabled 属性表示组件为只读，支持小数分值。此时若设置 show-score，则会在右侧显示目前的分值。可以提供 score-template 作为显示模板，模板为一个包含了 {value}
    的字符串，{value} 会被解析为分值。
    <el-rate
      v-model="value5"
      disabled
      show-score
      text-color="#ff9900"
      score-template="{value}">
    </el-rate>
  </div>
</template>

<script>
  export default {
    data() {
      return {
        value1: null,
        value2: null,
        value3: null,
        value4: null,
        value5: 3.7
      }
    }
  }
</script>

